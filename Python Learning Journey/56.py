# Using dir(), dict(), and help() functions in Python

# 1. dir() - Lists the attributes and methods of an object
print("Using dir() on a list:")
my_list = [1, 2, 3]
print(dir(my_list))  # Shows all the methods and attributes of a list
print("\n")

# 2. dict() - Creates a dictionary
print("Using dict() to create a dictionary:")
my_dict = dict(name="Alice", age=25, city="New York")
print(my_dict)
print("\n")

# 3. help() - Shows the documentation/help of a function or class
print("Using help() on the dict() function:")
help(dict)  # Shows detailed info about the dict() function

